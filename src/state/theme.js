/* ============================================================================
 * Imports
 * ========================================================================= */
import light from '../themes/light';
import dark from '../themes/dark';

const themes = {
  light, dark
}

/* ============================================================================
 * Actions
 * ========================================================================= */
const actions = {
  dark: () => ({ type: 'DARK' }),
  light: () => ({ type: 'LIGHT' })
};

/* ============================================================================
 * Reducers
 * ========================================================================= */
const reducer = (state = themes.light, action) => {

  switch (action.type) {

    case 'DARK':
      return themes.dark;

    case 'LIGHT':
      return themes.light;

    default:
      return themes.light;
  }
}

/* ============================================================================
 * Exports
 * ========================================================================= */
export { actions, reducer };