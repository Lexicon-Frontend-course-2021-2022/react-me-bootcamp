/* ============================================================================
 * Actions
 * ========================================================================= */
const actions = {
  login: () => ({ type: 'LOGIN' }),
  logout: () => ({ type: 'LOGOUT' })
};

/* ============================================================================
 * Reducers
 * ========================================================================= */
const reducer = (state = false, action) => {

  switch (action.type) {

    case 'LOGIN':
      return true;

    case 'LOGOUT':
      return false;

    default:
      return state;
  }
}

/* ============================================================================
 * Exports
 * ========================================================================= */
export { actions, reducer };