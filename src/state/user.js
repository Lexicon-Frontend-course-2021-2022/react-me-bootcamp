/* ============================================================================
 * Actions
 * ========================================================================= */
const actions = {
  login: (payload) => ({ type: 'LOGIN', payload }),
  logout: () => ({ type: 'LOGOUT' })
};

/* ============================================================================
 * Reducers
 * ========================================================================= */
const reducer = (state = null, action) => {

  switch (action.type) {

    case 'LOGIN':
      return action.payload;

    case 'LOGOUT':
      return null;

    default:
      return state;
  }
}

/* ============================================================================
 * Exports
 * ========================================================================= */
export { actions, reducer };